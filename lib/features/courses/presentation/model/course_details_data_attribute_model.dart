class CourseDetailsDataAttributeModel {
  String? name;
  String? description;
  int? active;
  int? resourceId;
  int? hours;
  int? price;
  double? rate;
  String? avatar;

  CourseDetailsDataAttributeModel({
    required this.name,
    required this.description,
    required this.active,
    required this.resourceId,
    required this.hours,
    required this.price,
    required this.rate,
    required this.avatar,
  });
}
